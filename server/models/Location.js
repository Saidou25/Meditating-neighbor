const { Schema, model } = require("mongoose");

const locationSchema = new Schema({
  username: {
    type: String,
    required: true,
  },
  longitude: {
    type: Number,
    required: true,
  },
  latitude: {
    type: Number,
    required: true,
  },
  city: {
    type: String,
    required: true,
  },
  state: {
    type: String,
    required: true,
  },
  country: {
    type: String,
    required: true,
  },
});

const Location = model("location", locationSchema);

module.exports = Location;
